//Repetir como um papagaio
//Crie um loop utilizando for que se repita 5 vezes. Dentro de cada repetição se deve mostrar no console a mensagem “Olá mundo”.

for (let repet = 1; repet <=5; repet++) {
    console.log('Olá Mundo!');
};

//Contando números ímpares
//Crie um loop for de 1 a 10, em que exiba no console apenas o valor das repetições de números ímpares. Dica: A ideia é que nas 
//repetições ímpares de 0 ao 10 (1,3,5,7,9) sejam mostrados os números das repetições no console.

for (let impares = 1; impares <=10; impares++) {
    console.log(impares + (impares +1));
};


